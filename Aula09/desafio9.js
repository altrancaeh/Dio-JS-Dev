/*
Crie um programa que dado um número, imprima sua tabuada.
*/

let numero = 5
for(let i = 1; i <= 10; i++) {
    console.log(`${numero} x ${i} = ${numero*i}`)
}

// Posição inicial é i, início da tabuada. Se ele não for menor ou igual a 10, ele realia a soma de si mesmo e multipilica  na linha 7 pelo número que aloquei na variável.